function NavIcon({ d }) {
  return (
    <svg className="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24" strokeWidth={1.8}>
      <path strokeLinecap="round" strokeLinejoin="round" d={d} />
    </svg>
  )
}

export default function DesktopSidebar({ className = '' }) {
  const user = useAuthStore((s) => s.user)
  const logout = useAuthStore((s) => s.logout)
  const navigate = useNavigate()
  const location = useLocation()
  const items = NAV_ITEMS[user?.role] || NAV_ITEMS.school

  const handleLogout = () => {
    logout()
    navigate('/login')
  }

  const isPathActive = (itemPath) => {
    const [path, search] = itemPath.split('?')
    const currentPath = location.pathname
    const currentSearch = location.search.substring(1) // remove '?'
    
    // Exact path match
    if (currentPath !== path) return false
    
    // If item has search params, current must have them too
    if (search) {
      return currentSearch.includes(search)
    }
    
    // If item has NO search params, current must have NO 'scan=true'
    return !currentSearch.includes('scan=true')
  }

  return (
    <aside className={`hidden lg:flex w-64 bg-white border-r border-gray-100 fixed h-full flex-col ${className}`}>
      {/* Logo */}
      <div className="p-6 border-b border-gray-100">
        <div className="flex items-center gap-3">
          <div className="w-10 h-10 bg-blue-600 rounded-xl flex items-center justify-center">
            <span className="text-white font-bold text-lg">Q</span>
          </div>
          <div>
            <h2 className="text-base font-bold text-gray-900">AI Question Hub</h2>
            <span className={`text-[10px] font-semibold px-2 py-0.5 rounded-full ${
              user?.subscription === 'pro'
                ? 'bg-purple-100 text-purple-700'
                : 'bg-gray-100 text-gray-500'
            }`}>
              {user?.subscription === 'pro' ? 'PRO' : 'FREE'}
            </span>
          </div>
        </div>
      </div>

      {/* Nav */}
      <nav className="flex-1 p-4 space-y-1">
        {items.map((item) => {
          const isActive = isPathActive(item.path)
          return (
            <NavLink
              key={item.path}
              to={item.path}
              className={`flex items-center gap-3 px-3 py-2.5 rounded-xl text-sm font-medium transition-all duration-200 ${
                isActive
                  ? 'bg-blue-50 text-blue-700 shadow-sm'
                  : 'text-gray-500 hover:bg-gray-50 hover:text-gray-900'
              }`}
            >
              <NavIcon d={item.icon} />
              {item.label}
            </NavLink>
          )
        })}
      </nav>

      {/* Upgrade banner (free users) */}
      {user?.subscription !== 'pro' && (
        <div className="mx-4 mb-4 p-4 bg-gradient-to-br from-blue-600 to-purple-600 rounded-2xl text-white">
          <p className="text-sm font-bold mb-1">Pro তে আপগ্রেড করুন</p>
          <p className="text-[11px] opacity-80 mb-3">আনলিমিটেড ফিচার পান মাত্র ২৯৯ টাকায়</p>
          <NavLink 
            to="/pricing"
            className="block w-full bg-white text-center text-blue-600 text-sm font-semibold py-2 rounded-xl hover:bg-blue-50 transition-colors"
          >
            আপগ্রেড
          </NavLink>
        </div>
      )}

      {/* User */}
      <div className="p-4 border-t border-gray-100">
        <div className="flex items-center gap-3">
          <div className="w-9 h-9 bg-blue-100 rounded-full flex items-center justify-center text-blue-600 font-semibold text-sm">
            {user?.name?.charAt(0) || 'U'}
          </div>
          <div className="flex-1 min-w-0">
            <p className="text-sm font-medium text-gray-900 truncate">{user?.name}</p>
            <p className="text-[11px] text-gray-400 truncate">{user?.email}</p>
          </div>
          <button
            onClick={handleLogout}
            className="p-2 text-gray-400 hover:text-red-500 transition-colors rounded-lg hover:bg-red-50"
            title="লগআউট"
          >
            <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24" strokeWidth={1.8}>
              <path strokeLinecap="round" strokeLinejoin="round" d="M15.75 9V5.25A2.25 2.25 0 0013.5 3h-6a2.25 2.25 0 00-2.25 2.25v13.5A2.25 2.25 0 007.5 21h6a2.25 2.25 0 002.25-2.25V15m3 0l3-3m0 0l-3-3m3 3H9" />
            </svg>
          </button>
        </div>
      </div>
    </aside>
  )
}
